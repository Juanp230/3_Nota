package unidad2.actividad4;

public class Sobrecarga {

    public int Sumar(int numA, int numB){
        return numA+numB;

    
    }
    public int Sumar(int numA, int numB, int numC){
        return numA+numB+numC;

    }
    public double sumar(double numA, double numB){
        return numA+numB;
    }
    

}